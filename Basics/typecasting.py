#Typecasting
#Typecasting is the process of converting a variable from one data type to another.
#For example, converting an integer to a float or a string to an integer.
#Typecasting is useful when you need to perform operations on variables of different data types.
#Typecasting can be done using the built-in functions of Python, such as int(), float(),
#str(), etc.
#Example: Converting an integer to a float
x = 5
y = float(x)
print(y)  # Output: 5.0
print(type(y))
#Example: Converting a string to an integer
s = "5"
y = int(s)
print(y)  # Output: 5
print(type(y))
b=True
print(b)
bi = int(b)
print(bi)
print(type(bi))

